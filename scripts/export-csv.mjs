rows.push('role,category,city,state,median_salary_usd,p25_usd,p75_usd,p90_usd,yoy_change_pct,cost_of_living_index,source,updated_at')

for (const role of ROLES) {
  const base = BASE_SALARIES[role.slug]
  if (!base) continue
  for (const city of CITIES) {
    const idx = city.costIndex
    rows.push([
      `"${role.label}"`,
      `"${base.category}"`,
      `"${city.label}"`,
      city.state,
      round5k(base.median * idx),
      round5k(base.p25 * idx),
      round5k(base.p75 * idx),
      round5k(base.p90 * idx),
      base.yoy,
      idx,
      'BLS OES 2025',
      'May 2026',
    ].join(','))
  }
}

